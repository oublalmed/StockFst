    private void ajtBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajtBtnActionPerformed
        Connection cn;
        PreparedStatement ps;  
        cn=connecter();
        
       try{
           if (cn==null){
               JOptionPane.showMessageDialog(this, "Pas de connexion");
           }
           else {
                if(IDCmd.getText().equals("") || prixCmd.getText().equals("") ||dateCmd.getText().equals("")|| qteCmd.getText().equals("") || libelleCmd.getText().equals(""))
                {JOptionPane.showMessageDialog(this, "SVP entrer vos donneés"); }
                else{               
           ps = cn.prepareStatement("insert into cammande values(?,?,?,?,?,?)");
           ps.setString(1,IDCmd.getText());
           ps.setString(2,libelleCmd.getText());
           ps.setString(3,qteCmd.getText());
           ps.setString(4,prixCmd.getText());
           ps.setString(5,dateCmd.getText());
           ps.setInt(6,CmbUtilisateur.getSelectedIndex()+1);           
           ps.executeUpdate();
           JOptionPane.showMessageDialog(this, "L'ajout est fait avec succés");
           }}
          }catch(Exception e){
       JOptionPane.showMessageDialog(this, "NULL");
       }
    }//GEN-LAST:event_ajtBtnActionPerformed

    private void cherBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cherBtnActionPerformed
        ArrayList<Commande> listCmd =new ArrayList<>();
        Connection cn;
        PreparedStatement ps;  
        cn=connecter();
        ResultSet rs;
       try{
           if (cn==null){
               JOptionPane.showMessageDialog(this, "Pas de connexion");
           }
           else {
           ps = cn.prepareStatement("select idCommande,libelle,qteCmd,prixCmd,dateCmd,nomUtilisateur from cammande c ,utilisateur u where c.idUtilisateur=u.idUtilisateur order by idCommande asc ");
           rs=ps.executeQuery();
           Commande cmd;
           while(rs.next()){
             cmd=new Commande(rs.getInt("idCommande"),rs.getString("libelle"),rs.getInt("qteCmd"),rs.getInt("prixCmd"),rs.getString("dateCmd"),rs.getString("nomUtilisateur"));
             listCmd.add(cmd);
           }
           }
         }catch(Exception Ex){}
       DefaultTableModel model=(DefaultTableModel) tab_Commande.getModel();
        Object[] row = new Object[6];
      for(int i=0;i<listCmd.size();i++)
      {
          row[0]=listCmd.get(i).getIdCmd();
          row[1]=listCmd.get(i).getLibelleCmd();
          row[2]=listCmd.get(i).getQteCmd();
          row[3]=listCmd.get(i).getPrixCmd();
          row[4]=listCmd.get(i).getDateCmd();
          row[5]=listCmd.get(i).getNomUtilisateur();
          model.addRow(row);
      }
    }//GEN-LAST:event_cherBtnActionPerformed

