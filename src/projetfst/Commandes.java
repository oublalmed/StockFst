    public void RemplirComboUtilisateur(){
        Connection cn;
        PreparedStatement ps;
        cn=connecter();
        try{
            if (cn==null){
                JOptionPane.showMessageDialog(this, "Pas de connexion");
            }
            else {
                ps=cn.prepareStatement("select nomUtilisateur from utilisateur ");
                ResultSet rs=ps.executeQuery();
                while(rs.next())
                {
                CmbUtilisateur.addItem(rs.getString("nomUtilisateur"));
                 }
        }
            }catch(Exception Ex){}
    }
